    const textarea = document.getElementById('content');

    if (emojiPickerBtn && emojiPicker && emojiGrid) {
        let currentCategory = 'smileys';

        // Toggle emoji picker
        emojiPickerBtn.addEventListener('click', function() {
            emojiPicker.style.display = emojiPicker.style.display === 'none' ? 'block' : 'none';
            if (emojiPicker.style.display === 'block') {
                loadEmojis(currentCategory);
            }
        });

        // Category switching
        document.querySelectorAll('.emoji-category-btn').forEach(btn => {
            btn.addEventListener('click', function() {
                // Remove active class from all buttons
                document.querySelectorAll('.emoji-category-btn').forEach(b => b.classList.remove('active'));
                // Add active class to clicked button
                this.classList.add('active');
                
                currentCategory = this.dataset.category;
                loadEmojis(currentCategory);
            });
        });

        // Load emojis for a category
        function loadEmojis(category) {
            emojiGrid.innerHTML = '';
            const emojis = emojiDatabase[category] || [];
            
            emojis.forEach(emoji => {
                const button = document.createElement('button');
                button.type = 'button';
                button.className = 'emoji-item';
                button.textContent = emoji;
                button.title = emoji;
                
                button.addEventListener('click', function() {
                    insertEmoji(emoji);
                });
                
                emojiGrid.appendChild(button);
            });
        }

        // Insert emoji into textarea
        function insertEmoji(emoji) {
            const cursorPos = textarea.selectionStart;
            const textBefore = textarea.value.substring(0, cursorPos);
            const textAfter = textarea.value.substring(cursorPos);
            
            textarea.value = textBefore + emoji + textAfter;
            textarea.focus();
            
            // Position cursor after emoji
            const newCursorPos = cursorPos + emoji.length;
            textarea.setSelectionRange(newCursorPos, newCursorPos);
            
            // Trigger input event for validation
            textarea.dispatchEvent(new Event('input', { bubbles: true }));
        }

        // Close picker when clicking outside
        document.addEventListener('click', function(e) {
            if (!emojiPicker.contains(e.target) && !emojiPickerBtn.contains(e.target)) {
                emojiPicker.style.display = 'none';
            }
        });

        // Load initial emojis
        loadEmojis(currentCategory);
    }
