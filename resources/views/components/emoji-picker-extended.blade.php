        document.addEventListener('click', function(e) {
            if (!emojiPicker.contains(e.target) && !emojiPickerBtn.contains(e.target)) {
                emojiPicker.style.display = 'none';
            }
        });

        // Load initial emojis
        loadEmojis(currentCategory);
    }
